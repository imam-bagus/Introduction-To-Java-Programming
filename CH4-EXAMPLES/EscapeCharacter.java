public class EscapeCharacter {

  public static void main(String[] args) {
    System.out.println("\t Imam Bagus Nugroho");
    System.out.println("\\ Imam Bagus Nugroho");
    System.out.println("\b Imam Bagus Nugroho");
    System.out.println("\n Imam Bagus Nugroho");
    System.out.println("\"Imam\" Bagus Nugroho");
  }
}
